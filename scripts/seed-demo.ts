/** Статический плейсхолдер из public/ — без API и внешних сервисов. */
function img(_n: number): string {
  return "/images/placeholder.svg";
}

async function main() {
  const adminEmail = ADMIN_EMAIL.trim().toLowerCase();
  const admin = await prisma.users.findFirst({
    where: { email: adminEmail },
    select: { id: true },
  });
  if (!admin) {
    console.error("Ошибка: админ bshevelev@mail.ru не найден. Сначала выполните: npx tsx scripts/ensure-admin.ts");
    process.exit(1);
  }
  const sellerId = admin.id;

  console.log("1. Очистка таблиц items, news, testimonials...");
  await prisma.messages.deleteMany();
  await prisma.cart_items.deleteMany();
  const [delItems, delNews, delTest] = await Promise.all([
    prisma.items.deleteMany(),
    prisma.news.deleteMany(),
    prisma.testimonials.deleteMany(),
  ]);
  console.log(`   Удалено: items=${delItems.count}, news=${delNews.count}, testimonials=${delTest.count}`);

  console.log("\n2. Создание 10 новостей...");
  for (const n of NEWS) {
    await prisma.news.create({ data: { title: n.title, body: n.body, is_published: true } });
  }
  console.log("   Создано 10 новостей");

  console.log("\n3. Создание 10 отзывов...");
  for (const t of TESTIMONIALS) {
    await prisma.testimonials.create({
      data: { author_name: t.author_name, text: t.text, rating: t.rating, is_active: true },
    });
  }
  console.log("   Создано 10 отзывов");

  console.log("\n4. Создание 10 товаров...");
  let imgSeed = 100;
  for (const it of ITEMS) {
    const count = 4 + Math.floor(Math.random() * 5); // 4–8 картинок
    const urls = Array.from({ length: count }, () => img(++imgSeed));
    await prisma.items.create({
      data: {
        seller_id: sellerId,
        title: it.title,
        description: it.desc,
        price: it.price,
        location: it.location,
        image_url: urls[0],
        image_urls: urls,
        status: "available",
      },
    });
  }
  console.log("   Создано 10 товаров с 4–8 фото");

  console.log("\n✓ Готово. Админ: bshevelev@mail.ru (пароль: 123456)");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
